#include "arithmeticfunctions.h"

// Compute x^3
int cube(int x){
    return x*x*x;
    //return -1
}

// Compute the maximum of x and y
int max(int x, int y){
    if(x>y) return x;
    else return y;
    //return -1;
}

// Compute x - y
int difference(int x, int y){
    return x-y;
    //return -1;
}